let puntaje = 5;
console.log(puntaje);

console.log('******** Incremento ********');
puntaje++;
console.log(`puntaje++ : ${puntaje}`);
++puntaje;
console.log(`++puntaje : ${puntaje}`);
puntaje += 3;
console.log(`puntaje += : ${puntaje}`);

console.log('******** Decremento ********');
puntaje--;
console.log(`puntaje-- : ${puntaje}`);
--puntaje;
console.log(`--puntaje : ${puntaje}`);
puntaje -= 3;
console.log(`puntaje -= : ${puntaje}`);